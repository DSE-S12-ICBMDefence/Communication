function att = attenuationDryAir(f, t, p, ro)
    % approximation valid up to 54 GHz
    e = ro * (t + 273.15) / 216.7;
    ptot = p + e;

    rp = ptot / 1013;
    rt = 288 / (273 + t);

    xi1 = phi(rp, rt, 0.0717, -1.8132, 0.0156, -1.6515);
    xi2 = phi(rp, rt, 0.5146, -4.6368, -0.1921, -5.7416);
    xi3 = phi(rp, rt, 0.3414, -6.5851, 0.2130, -8.5854);

    gamma0 = (((7.2 * rt^(2.8)) ./ (f.^2 + 0.34 * rp^2 * rt^(1.6))) ...
        + ((0.62 * xi3) ./ ((54 - f).^(1.16 * xi1) + 0.83 * xi2))) .* f.^2 * rp^2 * 1e-3;

    t1 = 4.64 / (1 + 0.066 * rp^(-2.3)) * exp(-((f - 59.7) / (2.87 + 12.4 * exp(-7.9 * rp))).^2);
    t2 = 0.14 * exp(2.12 * rp) ./ ((f - 118.75).^2 + 0.031 * exp(2.2 * rp));
    t3 = 0.0114 ./ (1 + 0.14 * rp^(-2.6)) .* f .* (-0.0247 + 0.0001 * f + 1.61e-6 * f.^2) ...
        ./ (1 - 0.0169 * f + 4.1e-5 * f.^2 + 3.2e-7 * f.^3);

    h0 = 6.1 / (1+0.17* rp^(1.1)) * (1+t1 +t2 + t3);
    att = h0 .* gamma0;
end